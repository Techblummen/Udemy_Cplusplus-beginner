#include <iostream>
using namespace std;

int main() {

    for (int i=0; i<10; i++){
        cout << "Step " << i << endl;
    }

    //The for loop is very useful and this is apparently only the most common way of using this loop.
	//It is nice you can create the counting variable (i), establish its limits and add the increment
	//all within one line of code.

    return 0;
}

